    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        String cadena = JOptionPane.showInputDialog(this, "Ingrese el nombre");
        boolean estado = true;
        
        for(Datos datos : listadatos ){
            if(cadena.equals(datos.getNombre())){
                name.setText("Nombre: " + datos.getNombre());
                age.setText("Edad: " + String.valueOf(datos.getEdad()));
                mail.setText("Correo: " + datos.getCorreo());
                estado = false;
                break;
            }
        }
        
        if(estado){
            JOptionPane.showMessageDialog(this, "No se encontro el nombre");
        }else{
            JOptionPane.showMessageDialog(this, "Se encontro el nombre"); 
        }
        
      
    }//GEN-LAST:event_btnbuscarActionPerformed

